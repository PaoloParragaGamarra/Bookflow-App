export class User {
  id: any;
  firstName: any;
  lastName: any;
  age: any;
  email: any;
  description: any
  constructor(id: any, firstName: any, lastName: any, age: any,email:any, description: any) {
    this.id = id;
    this.firstName = firstName;
    this.lastName = lastName;
    this.age = age;
    this.email = email;
    this.description = description;
  }
}
